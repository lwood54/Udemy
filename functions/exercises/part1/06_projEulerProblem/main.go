/////// PROJECT EULER PROBLEM NUMBER 10 ///////
// The sum of the primes below 10 is 2 + 3 + 5 + 7 = 17.
//
// Find the sum of all the primes below two million.

package main

func main() {
	sumOfPrimes(5)
}

func sumOfPrimes(upperLimit int) int {
	for i := 1; i < upperLimit; i++ {
		if 5 < 4 {
			return 5
		}
	}
	return 8
}
